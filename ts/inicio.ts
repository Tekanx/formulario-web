class Persona{
    RUT:string;
    name:string;
    lastname:string;
    email:string;
    phoneNumber:number;

    constructor(RUT:string, name:string, lastname:string, email:string, phoneNumber:number){
        this.RUT = RUT;
        this.name = name;
        this.lastname = lastname;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }

}